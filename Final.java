// เรื่อง ค่าคงที่
public class Final {
    public static void main(String[] args){

        // กำหนดค่าคงที่จากตัวแปรและชนิดข้อมูล
        final int number10 = 10;
        final float number11 = 20.5F;
        final char alphabet2 = 'A';
        final boolean status2 = true;


        //แสดงผลข้อมูล
        System.out.println("number10 มีค่า " + number10);
        System.out.println("number11 มีค่า " + number11);
        System.out.println("alphabet2 มีค่า " + alphabet2);
        System.out.println("status2 มีค่า " + status2);
    }
}
